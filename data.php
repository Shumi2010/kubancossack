<center><h2> Знаменательные даты Кубанского казачества</h2></center>
* В 1798 году донскому старшине было пожаловано дворянство;<br>
* в 1775 году правительство Екатерины II упразднило Запорожскую Сечь;<br>
* в 1788 году из части украинских казаков формируется Черноморское казачье войско. Через 4 года оно переведено на Кубань; <br>
* в 1797 году вспухнуло восстание двух казачьих полков в Екатеринодаре, т.н. восстание "сиромы" - казачьей бедноты;<br>
* 26 мая 1835 году Николай I утвердил положение об управлении Донского войска, по которому каждый казак-мужчина становился пожизненным владельцем земельного
участка в 30 десятин;<br>
* положение о Черноморском казачьем войске было утверждено 1 июня 1842 года;<br>
* 2 октября 1827 года Николай I объявил своего сына, наследника Александра, атаманом всех казачьих войск;<br>
* в 1868 году правительство разрешило всем желающим приобретать в пределах казачьих войск недвижимую собственность; <br>
