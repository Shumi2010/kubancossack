<center>Цифры и факты о жизни кубанских казаков</center>
* в 1913 году в области имелось 662 248 земледельческих орудий. Казакам принадлежало 476 248, а иногородним только 186 059.<br>
* в 1913 году общая площадь Кубанской области составляла 8660 тысяч десятин земли, из них казачьему войску принадлежало 6781 тысяча десятин.<br>
* в 1913 году средний душевой земельный "пай" кубанского казака равнялся 9.29 десятины.<br>
* в 1913 году в европейской части страны насчитывалось 266929 богатых казачьих хозяйств;<br>
* в 1912 году главным образом на покубку строевых коней для бедных казаков было израсходовано 456 тысяч рублей, а в 1915 более 1872 тысячи;<br>
* 

<strong>Некоторые примеры жизни казаков</strong><br>
Семья казака Алексея Коваленко из станицы Апшеронской на 4х человек имела три дома с постройками, три пары быков, три коровы, две лошади, 50 колодок пчел, паевой надел 6,5 десятины.<br><br>

